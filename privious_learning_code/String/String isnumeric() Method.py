str = u"this2009";
print(str.isnumeric())

str = u"23443434";
print(str.isnumeric())

# Description
#
# The method isnumeric() checks whether the string consists of only numeric characters. This method is present only
# on unicode objects.
#
# Note − To define a string as Unicode, one simply prefixes a 'u' to the opening quotation mark of the assignment.
# Below is the example. Syntax
#
# Following is the syntax for isnumeric() method −
#
# str.isnumeric()
#
# Parameters
#
#     NA
#
# Return Value
#
# This method returns true if all characters in the string are numeric, false otherwise.
